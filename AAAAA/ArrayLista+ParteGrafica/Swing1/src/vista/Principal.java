    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing

          String [] botones={"Cerrar","Cancelar"}; // botones que nos va a  // aparecer en el cuadro de dialogo        
          int eleccion; 
          // para guardar que ha seleccionado el usuario         
          eleccion= JOptionPane.showOptionDialog(this, " ¿ Realmente desea salir", "Cerrar Aplicacion"  , 0,0, null, botones,this);
          // ventana de dialogo , donde preguntamos al usuario que desea hacer 
          
            if ( eleccion==JOptionPane.YES_OPTION){// el usuario ha seleccionado si
                dispose();             
                System.exit(0); 
            }else{ // informos al usuario que ha seleccionado cancelar y no se hace nada.
                JOptionPane.showMessageDialog( this," Proceso de cierre cancelado", "Cerrar Aplicación", JOptionPane.INFORMATION_MESSAGE);     
            }    
            
            
    
    }//GEN-LAST:event_formWindowClosing

